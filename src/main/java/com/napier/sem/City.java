package com.napier.sem;

/**
 * Represents a city
 */
public class City {
    /**
     * City ID
     */
    public int id;

    /**
     * City Name
     */
    public String name;

    /**
     * City CountryCode
     */
    public String countryCode;

    /**
     * City District
     */
    public String district;

    /**
     * City Population
     */
    public int population;
}